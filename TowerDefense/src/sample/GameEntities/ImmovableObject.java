package sample.GameEntities;

import sample.GameObject;

public abstract class ImmovableObject implements GameObject {
    public int i, j;

    public int getI() {
        return i;
    }

    public void setI(int i) {
        this.i = i;
    }

    public int getJ() {
        return j;
    }

    public void setJ(int j) {
        this.j = j;
    }
}
